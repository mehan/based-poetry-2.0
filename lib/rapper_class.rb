class Rapper

	attr_accessor :name, :pussy_score, :bitch_score, :ho_score, :song_count

	def initialize(rapper_name, rapper_pussy_score, rapper_bitch_score, rapper_ho_score, rapper_song_count) 
		@name = rapper_name
		@pussy_score = rapper_pussy_score
		@bitch_score = rapper_bitch_score
		@ho_score = rapper_ho_score
    @song_count = rapper_song_count
	end

end